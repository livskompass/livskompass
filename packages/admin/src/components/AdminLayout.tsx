import { useState } from 'react'
import { Outlet, Link, useLocation, useNavigate } from 'react-router-dom'
import { useMutation, useQueryClient, useQuery } from '@tanstack/react-query'
import { logout, setAuthToken, getMe } from '../lib/api'

const navigation = [
  { name: 'Dashboard', href: '/dashboard', icon: HomeIcon },
  { name: 'Pages', href: '/sidor', icon: DocumentIcon },
  { name: 'Posts', href: '/nyheter', icon: NewspaperIcon },
  { name: 'Courses', href: '/utbildningar', icon: AcademicCapIcon },
  { name: 'Bookings', href: '/bokningar', icon: TicketIcon },
  { name: 'Products', href: '/material', icon: ShoppingBagIcon },
  { name: 'Media', href: '/media', icon: PhotoIcon },
  { name: 'Messages', href: '/meddelanden', icon: EnvelopeIcon },
  { name: 'Users', href: '/anvandare', icon: UsersIcon, adminOnly: true },
  { name: 'Settings', href: '/installningar', icon: CogIcon },
]

export default function AdminLayout() {
  const [sidebarOpen, setSidebarOpen] = useState(false)
  const location = useLocation()
  const navigate = useNavigate()
  const queryClient = useQueryClient()

  const { data: meData } = useQuery({
    queryKey: ['me'],
    queryFn: getMe,
  })

  const currentUser = meData?.user
  const isAdmin = currentUser?.role === 'admin'

  const logoutMutation = useMutation({
    mutationFn: logout,
    onSuccess: () => {
      setAuthToken(null)
      queryClient.clear()
      navigate('/login')
    },
  })

  return (
    <div className="min-h-screen flex">
      {/* Mobile sidebar backdrop */}
      {sidebarOpen && (
        <div
          className="fixed inset-0 bg-gray-600 bg-opacity-75 z-20 lg:hidden"
          onClick={() => setSidebarOpen(false)}
        />
      )}

      {/* Sidebar */}
      <aside
        className={`fixed inset-y-0 left-0 z-30 w-64 bg-gray-900 transform transition-transform duration-200 ease-in-out lg:translate-x-0 lg:static ${
          sidebarOpen ? 'translate-x-0' : '-translate-x-full'
        }`}
      >
        <div className="flex items-center justify-between h-16 px-4 bg-gray-800">
          <Link to="/dashboard" className="text-xl font-bold text-white">
            Livskompass
          </Link>
          <button
            className="lg:hidden text-gray-400 hover:text-white"
            onClick={() => setSidebarOpen(false)}
          >
            <XIcon className="h-6 w-6" />
          </button>
        </div>

        <nav className="mt-4 px-2 space-y-1">
          {navigation
            .filter((item) => !item.adminOnly || isAdmin)
            .map((item) => {
              const isActive = location.pathname.startsWith(item.href)
              return (
                <Link
                  key={item.name}
                  to={item.href}
                  className={`flex items-center px-3 py-2 rounded-lg text-sm font-medium transition-colors ${
                    isActive
                      ? 'bg-gray-800 text-white'
                      : 'text-gray-300 hover:bg-gray-800 hover:text-white'
                  }`}
                >
                  <item.icon className="h-5 w-5 mr-3" />
                  {item.name}
                </Link>
              )
            })}
        </nav>

        <div className="absolute bottom-0 left-0 right-0 p-4">
          <button
            onClick={() => logoutMutation.mutate()}
            className="w-full flex items-center px-3 py-2 text-gray-300 hover:bg-gray-800 hover:text-white rounded-lg text-sm font-medium transition-colors"
          >
            <LogoutIcon className="h-5 w-5 mr-3" />
            Log out
          </button>
        </div>
      </aside>

      {/* Main content */}
      <div className="flex-1 flex flex-col min-w-0">
        {/* Mobile header */}
        <header className="lg:hidden bg-white shadow-sm h-16 flex items-center px-4">
          <button
            className="text-gray-500 hover:text-gray-700"
            onClick={() => setSidebarOpen(true)}
          >
            <MenuIcon className="h-6 w-6" />
          </button>
          <span className="ml-4 text-lg font-semibold text-gray-900">
            Livskompass Admin
          </span>
        </header>

        <main className="flex-1 p-6 overflow-auto">
          <Outlet />
        </main>
      </div>
    </div>
  )
}

